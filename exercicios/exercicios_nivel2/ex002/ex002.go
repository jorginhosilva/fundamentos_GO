package main

import "fmt"

func main() {
	num1 := 30
	num2 := 35

	fmt.Println(num1 == num2) // igual
	fmt.Println(num1 != num2) // diferente
	fmt.Println(num1 <= num2) // menor ou igual
	fmt.Println(num1 < num2) // menor
	fmt.Println(num1 >= num2) // maior ou igual
	fmt.Println(num1 > num2) // maior

}