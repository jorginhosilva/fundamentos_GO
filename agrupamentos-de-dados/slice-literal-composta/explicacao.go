package main

func explicacao() {
	// slice é uma estrutura de dados dinâmica que representa 
	// uma vista sobre um array subjacente, os slices não tem
	// tamanho fixo, que é o caso dos arrays
	// um slice consiste em três partes:
	// Ponteiro -> Aponta para o primeiro elemento do array subjacente.
	// Tamanho -> Indica o número de elementos acessíveis no slice.
	// Capacidade -> Indica o tamanho total do array subjacente.
	// operações com Slices:
	// Append -> Adiciona elementos ao final do slice.
	// Copy -> Copia elementos de um slice para outro.
	// Make -> Cria um slice com tamanho e capacidade especificados
	// Len -> Retorna o tamanho do slice
	// Cap -> Retorna a capacidade do slice
}